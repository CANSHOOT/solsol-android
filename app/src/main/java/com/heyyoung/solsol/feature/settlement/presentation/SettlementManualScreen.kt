// üéØ ÏÉàÎ°úÏö¥ Ìï®Ïàò - ÌôîÎ©¥ ÌëúÏãúÏö© Í∏àÏï° Ìè¨Îß∑ÌåÖ
private fun formatDisplayAmount(amount: String): String {
    return when {
        amount.isEmpty() -> ""
        amount.length <= 3 -> amount
        else -> {
            try {
                val number = amount.toLongOrNull() ?: 0L
                String.format("%,d", number)
            } catch (e: Exception) {
                Log.w(TAG, "Ìè¨Îß∑ÌåÖ Ïò§Î•ò: $amount", e)
                amount // Ïò§Î•ò Ïãú ÏõêÎ≥∏ Î∞òÌôò
            }
        }
    }
}

