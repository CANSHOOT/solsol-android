@Composable
private fun CenterLightDisplay(
    isLightOn: Boolean,
    isFinished: Boolean,
    isWinner: Boolean
) {
    Box(
        modifier = Modifier.size(300.dp),
        contentAlignment = Alignment.Center
    ) {
        // Î∞∞Í≤Ω Ïõê
        Box(
            modifier = Modifier
                .size(250.dp)
                .background(Color(0xFF6B7280).copy(alpha = 0.1f), CircleShape)
                .shadow(4.dp, CircleShape)
        )

        // Î∂àÎπõ
        if (isLightOn || isFinished) {
            Box(
                modifier = Modifier
                    .size(200.dp)
                    .background(
                        when {
                            isFinished && isWinner -> Color(0xFFFFC107).copy(alpha = 0.9f)
                            isLightOn -> Color(0xFF8B5FBF).copy(alpha = 0.8f)
                            else -> Color(0xFF6B7280).copy(alpha = 0.2f)
                        },
                        CircleShape
                    )
                    .shadow(if (isLightOn || (isFinished && isWinner)) 20.dp else 8.dp, CircleShape)
            )
        }

        // Ï§ëÏïô ÏïÑÏù¥ÏΩò/ÌÖçÏä§Ìä∏
        Column(horizontalAlignment = Alignment.CenterHorizontally) {
            when {
                isFinished && isWinner -> {
                    Text("üèÜ", fontSize = 48.sp); Spacer(Modifier.height(12.dp))
                    Text("ÎãπÏ≤®!", fontSize = 24.sp, fontWeight = FontWeight.Bold, color = Color(0xFFF59E0B))
                }
                isFinished && !isWinner -> Text("‚ö™", fontSize = 48.sp, color = Color(0xFF999999))
                isLightOn -> Text("üí°", fontSize = 48.sp)
                else -> Text("‚ö™", fontSize = 48.sp, color = Color(0xFF999999))
            }
        }
    }
}

